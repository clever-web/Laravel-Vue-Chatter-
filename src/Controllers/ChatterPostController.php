        $offset = 0;
        if($request->total){
            $total = $request->total;
        }
        if($request->offset){
            $offset = $request->offset;
        }
        $posts = Post::with('user')->orderBy('created_at', 'DESC')->take($total)->offset($offset)->get();
        return response()->json($posts);
    }

